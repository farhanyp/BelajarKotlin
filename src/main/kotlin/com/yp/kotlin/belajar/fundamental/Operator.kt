package com.yp.kotlin.belajar.fundamental

fun main(){
    // Operator matematika +, -, *, /, %

    // keluaran integer
    var angka = 10/3
    println(angka)

    // keluaran double
    var angka1 = 10.0/3.0
    println(angka1)

    // urutan operasi matematika
    var angka2 = 10 + 10 / 3
    println(angka2)

    println()
    println()

    // angka ditambah 1
    angka++
    println(angka)

    // angka dikurang 1
    angka++
    println(angka)

    // angka dijadikan minus
    println(-angka)
}